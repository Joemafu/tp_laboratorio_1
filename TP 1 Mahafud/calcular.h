#include <stdio.h>
#include <stdlib.h>

/** \brief Toma dos flotantes, los suma y devuelve resultado flotante.
 *
 * \param float
 * \param float
 * \return float
 *
 */
float sumar(float, float);

/** \brief Toma dos flotantes, le resta el segundo y devuelve resultado flotante.
 *
 * \param float
 * \param float
 * \return float
 *
 */
float restar(float, float);


/** \brief Toma dos flotantes, los multiplica y devuelve resultado flotante.
 *
 * \param float
 * \param float
 * \return float
 *
 */
float multiplicar(float, float);


/** \brief Toma dos flotantes, divide el primero por el segundo y devuelve resultado flotante. Devuelve 0 en caso de error (división por 0).
 *
 * \param float
 * \param float
 * \return float
 *
 */
float dividir(float, float);


/** \brief Toma un entero, lo procesa y devuelve el factorial como double (De este modo puede calcular números más altos).
 *
 * \param int
 * \return double
 *
 */
double sacarFactorial(int);
